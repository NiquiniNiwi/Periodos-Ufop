    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Login - Padr√£o admin admin sempre entra, o resto depende de uma lista gerada a partir do cadastro de usuario.
        if (LoginText.getText().equals(adm.getLogin()) && SenhaText.getText().equals(adm.getSenha())){//Tipo administrador
            this.setEnabled(false);
            this.setVisible(false);
            new JanelaAdmin(this, dent, con, pgc, fun, pacientes, ass, geral).setVisible(true);
        }
        else if(geral.size() != 0){
            for (int index = 0; index < geral.size(); index++){
                if (LoginText.getText().equals(geral.get(index).getLogin()) && SenhaText.getText().equals(geral.get(index).getSenha())){
                    this.setEnabled(false);
                    this.setVisible(false);
                    new JanelaUserGeral(this, dent).setVisible(true);
                    break;
                }
            }
        }
        else if(ass.size() != 0){
            for (int index = 0; index < ass.size(); index++){
                if (LoginText.getText().equals(ass.get(index).getLogin()) && SenhaText.getText().equals(ass.get(index).getSenha())){
                    this.setEnabled(false);
                    this.setVisible(false);
                    new JanelaAssistenteAdmin(this, dent, con, pgc, fun).setVisible(true);
                    break;
                }
            }
        }
        //Caso a entrada seja invalida uma mensagem de erro aparece na tela.
        else{
            JOptionPane.showMessageDialog(null, "Acesso Negado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

