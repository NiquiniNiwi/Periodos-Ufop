    private void EfetuarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EfetuarPagamentoActionPerformed
        // Efetua o pagamento do funcionario selecionado na tela anterior.
        String value = JOptionPane.showInputDialog("Valor de pagamento: "); 
        if(!"".equals(value)){
            fun.setValorPg( Double.parseDouble(value));
            String descri = JOptionPane.showInputDialog("Descriss√£o: ");
            fun.setDesriPG(descri);
            String data = JOptionPane.showInputDialog("Data: ");
            fun.setDatasPg(data);
            String out = "";
            for(int index = 1; index < fun.getValorPg().size(); index++){ 
                out = out + fun.getDatasPg().get(index) +" "+ fun.getValorPg().get(index) +" "+ fun.getDescriPg().get(index) + "\n";
            }
            JOptionPane.showMessageDialog(null, out);
        }
    }//GEN-LAST:event_EfetuarPagamentoActionPerformed

