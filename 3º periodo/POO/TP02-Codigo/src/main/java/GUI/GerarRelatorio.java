    private void RGastosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RGastosActionPerformed
        // Mostra todos os gastos por conta
        String out = "";
        for(int i = 0; i < pc.getContasClinica().size(); i++){
                out = out + pc.getValoresConta(pc).get(i) + "\n";
        }
        JOptionPane.showMessageDialog(null, out);

    }//GEN-LAST:event_RGastosActionPerformed

    private void RGFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RGFActionPerformed
        // Mostra todos os gastos com funcionarios
        String out = "";
        for(int i = 0; i < fun.size(); i++){
            out = out + fun.get(i).getNome() + ": \n";
            for(int index = 0; index < fun.get(i).getValorPg().size(); index++){ 
                out = out + fun.get(i).getDatasPg().get(index) +" "+ fun.get(i).getDatasPg().get(index) +" "+ fun.get(i).getDatasPg().get(index) + "\n";
            }
        }
        for(int i = 0; i < dent.size(); i++){
            out = out + dent.get(i).getNome() + ": \n";
            for(int index = 0; index < dent.get(i).getValorPg().size(); index++){ 
                out = out + dent.get(i).getDatasPg().get(index) +" "+ dent.get(i).getDatasPg().get(index) +" "+ dent.get(i).getDatasPg().get(index) + "\n";
            }
        } 
        JOptionPane.showMessageDialog(null, out);
    }//GEN-LAST:event_RGFActionPerformed

